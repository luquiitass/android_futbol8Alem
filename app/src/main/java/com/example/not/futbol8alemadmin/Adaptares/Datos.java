package com.example.not.futbol8alemadmin.Adaptares;

public class Datos {
    private String nombre;
    private String dato;

    public Datos(String nombre, String dato) {
        this.nombre = nombre;
        this.dato = dato;
    }

    public String getDato() {
        return dato;
    }

    public String getNombre() {
        return nombre;
    }
}
